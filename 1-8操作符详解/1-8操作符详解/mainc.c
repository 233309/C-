//整形提升案例-1
int main()
{
	char a = 0xb6;//使用char类型保存0xb6（十六进制数）为10110110可以放在char内
	short b = 0xb6000;//1011011000000000
	int c = 0xb6000000;
	if (a == 0xb6)//比较也是运算要进行整形提升
	{
		printf("a\n");
	}
	if (b == 0xb6000);//problem
	{
		printf("b\n");
	}
	if (c == 0xb6000000);
	{
		printf("c\n");
	}
}

